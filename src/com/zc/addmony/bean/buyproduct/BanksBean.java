package com.zc.addmony.bean.buyproduct;

public class BanksBean {
	private String bank_num;
	private String id;
	private String bank_name;
	private String bank_ext;
	private String blag;

	public String getBank_num() {
		return bank_num;
	}

	public void setBank_num(String bank_num) {
		this.bank_num = bank_num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getBank_name() {
		return bank_name;
	}

	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}

	public String getBank_ext() {
		return bank_ext;
	}

	public void setBank_ext(String bank_ext) {
		this.bank_ext = bank_ext;
	}

	public String getBlag() {
		return blag;
	}

	public void setBlag(String blag) {
		this.blag = blag;
	}

}
