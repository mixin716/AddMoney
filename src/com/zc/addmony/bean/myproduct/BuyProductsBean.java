package com.zc.addmony.bean.myproduct;

/** 购买的基金bean */
public class BuyProductsBean {

	String mName;// 基金名称
	String mWf;// 万份收益
	String mHave;// 持有金额
	String mYestorday;// 昨日收益
	String mNot;// 未结算收益
	String mRedeem;// 赎回中
	String mBank;// 银行卡
	String mBankName;// 银行名字
	String shuhui;// 赎回标识

	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

	public String getmWf() {
		return mWf;
	}

	public void setmWf(String mWf) {
		this.mWf = mWf;
	}

	public String getmHave() {
		return mHave;
	}

	public void setmHave(String mHave) {
		this.mHave = mHave;
	}

	public String getmYestorday() {
		return mYestorday;
	}

	public void setmYestorday(String mYestorday) {
		this.mYestorday = mYestorday;
	}

	public String getmNot() {
		return mNot;
	}

	public void setmNot(String mNot) {
		this.mNot = mNot;
	}

	public String getmRedeem() {
		return mRedeem;
	}

	public void setmRedeem(String mRedeem) {
		this.mRedeem = mRedeem;
	}

	public String getmBank() {
		return mBank;
	}

	public void setmBank(String mBank) {
		this.mBank = mBank;
	}

	public String getShuhui() {
		return shuhui;
	}

	public void setShuhui(String shuhui) {
		this.shuhui = shuhui;
	}

	public String getmBankName() {
		return mBankName;
	}

	public void setmBankName(String mBankName) {
		this.mBankName = mBankName;
	}

}
